// Normalizar todas las keys del diccionario
const institucionesNormalizadas = {};
for (const muni in institucionesPorMunicipio) {
    const keyNormalizada = muni
        .normalize("NFD") // Descompone (ej: Á -> A + ´)
        .replace(/[\u0300-\u036f]/g, "") // Elimina el acento (se queda en A)
        .trim()
        .toUpperCase();

    institucionesNormalizadas[keyNormalizada] = institucionesPorMunicipio[muni];
}


// ✅ ESTO ES LO CRÍTICO: Asignar el objeto con las claves SIN ACENTOS
window.institucionesPorMunicipio = institucionesNormalizadas;

